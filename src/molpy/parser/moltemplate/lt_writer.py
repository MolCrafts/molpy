def _collect_atomtypes(ff: ForceField) -> list[AtomType]:
    seen: dict[str, AtomType] = {}
    for style in ff.styles.bucket(AtomStyle):
        for at in style.types.bucket(AtomType):
            if at.name not in seen:
                seen[at.name] = at
    return list(seen.values())


def _atom_name_map(atomistic: Atomistic) -> dict[int, str]:
    """Assign unique moltemplate ``$atom:`` ids to every atom.

    Always emits ``a<index>``-style names so no two atoms collide even
    when the underlying ``name`` attribute repeats (e.g. multiple water
    molecules each with ``O``/``H1``/``H2``).
    """
    out: dict[int, str] = {}
    for idx, atom in enumerate(atomistic.atoms, start=1):
        out[id(atom)] = f"a{idx}"
    return out


def _sanitise_id(s: str) -> str:
    """Make ``s`` a safe moltemplate ``$atom:`` identifier."""
    out = []
    for ch in s:
        if ch.isalnum() or ch in "_./":
            out.append(ch)
        else:
            out.append("_")
    result = "".join(out)
    return result or "x"


def _fmt(v: float) -> str:
    """Format floats compactly but unambiguously."""
    try:
        f = float(v)
    except (TypeError, ValueError):
        return str(v)
    if abs(f) >= 1e7 or (f != 0.0 and abs(f) < 1e-4):
        return f"{f:.6e}"
    return f"{f:g}"
