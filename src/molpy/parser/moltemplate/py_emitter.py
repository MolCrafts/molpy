_DATA_ATOM_RE = re.compile(
    r"""
    ^\s*\$atom(?::(?P<id>\S+))?\s+
    (?:\$mol(?::\S+)?\s+)?
    @atom(?::(?P<type>\S+))?\s+
    (?P<charge>-?\d+\.?\d*(?:[eE][+-]?\d+)?)\s+
    (?P<x>-?\d+\.?\d*(?:[eE][+-]?\d+)?)\s+
    (?P<y>-?\d+\.?\d*(?:[eE][+-]?\d+)?)\s+
    (?P<z>-?\d+\.?\d*(?:[eE][+-]?\d+)?)
    """,
    re.VERBOSE,
)
_DATA_BOND_TYPED_RE = re.compile(
    r"^\s*\$bond:(?P<id>\S+)\s+@bond:(?P<type>\S+)\s+\$atom:(?P<a>\S+)\s+\$atom:(?P<b>\S+)"
)
_DATA_BOND_LIST_RE = re.compile(
    r"^\s*\$bond:(?P<id>\S+)\s+\$atom:(?P<a>\S+)\s+\$atom:(?P<b>\S+)"
)
_DATA_ANGLE_RE = re.compile(
    r"^\s*\$angle:(?P<id>\S+)\s+@angle:(?P<type>\S+)\s+\$atom:(?P<a>\S+)\s+\$atom:(?P<b>\S+)\s+\$atom:(?P<c>\S+)"
)
_DATA_DIHE_RE = re.compile(
    r"^\s*\$dihedral:(?P<id>\S+)\s+@dihedral:(?P<type>\S+)\s+"
    r"\$atom:(?P<a>\S+)\s+\$atom:(?P<b>\S+)\s+\$atom:(?P<c>\S+)\s+\$atom:(?P<d>\S+)"
)
_DATA_IMPR_RE = re.compile(
    r"^\s*\$improper:(?P<id>\S+)\s+@improper:(?P<type>\S+)\s+"
    r"\$atom:(?P<a>\S+)\s+\$atom:(?P<b>\S+)\s+\$atom:(?P<c>\S+)\s+\$atom:(?P<d>\S+)"
)


def _class_emission_order(
    classes: dict[str, ClassDef],
    doc: Document,
) -> list[str]:
    order: list[str] = []
    seen: set[str] = set()

    def visit(name: str, stack: tuple[str, ...] = ()) -> None:
        if name in seen or name in stack or name not in classes:
            return
        cls = classes[name]
        for base in cls.bases:
            visit(base, stack + (name,))
        for s in cls.statements:
            if isinstance(s, NewStmt):
                if s.class_name == "random":
                    for ch in s.random_choices:
                        visit(ch.class_name, stack + (name,))
                else:
                    visit(s.class_name, stack + (name,))
        if name not in seen:
            seen.add(name)
            order.append(name)

    for name in classes:
        visit(name)
    return order


def _apply_atom_replace_static(raw_type: str, replacements) -> str:
    if not replacements or not raw_type:
        return raw_type
    for pat, new in replacements:
        if pat.match(raw_type):
            return new
    return raw_type


def _resolve_class_body(
    cls: ClassDef, class_map: dict[str, ClassDef], stack: tuple[str, ...] = ()
) -> list:
    if cls.name in stack:
        return []
    body: list = []
    for base in cls.bases:
        base_cls = class_map.get(base)
        if base_cls is None:
            continue
        body.extend(_resolve_class_body(base_cls, class_map, stack + (cls.name,)))
    for s in cls.statements:
        if not isinstance(s, ClassDef):
            body.append(s)
    return body


def _emit_class_builder(
    cls: ClassDef,
    *,
    class_map: dict[str, ClassDef],
    atom_replacements,
) -> str:
    fn_name = _sanitize_ident("build_" + cls.name)
    lines: list[str] = [
        f"def {fn_name}() -> Atomistic:",
        f'    """Template builder for moltemplate class ``{cls.name}``."""',
        "    tmpl = Atomistic()",
        "    atoms: dict[str, object] = {}",
    ]

    effective = _resolve_class_body(cls, class_map)

    # Pass 1: inner `new` statements
    for s in effective:
        if isinstance(s, NewStmt) and s.class_name != "random":
            sub_fn = _sanitize_ident("build_" + s.class_name)
            lines.append(f"    sub = {sub_fn}()")
            for t in s.transforms:
                lines.append(f"    {_transform_call('sub', t)}")
            lines.extend(
                [
                    "    for _a in list(sub.atoms):",
                    "        _base = _a.get('name', '')",
                    f"        _key = {s.instance_name!r} + '/' + _base",
                    "        _a['name'] = _key",
                    "        atoms[_key] = _a",
                    "    tmpl += sub",
                ]
            )

    # Pass 2: Data Atoms / Bonds / Angles / Dihedrals / Impropers
    anon = 0
    for s in effective:
        if not isinstance(s, (WriteBlock, WriteOnceBlock)):
            continue
        if s.section == "Data Atoms":
            for ln in s.body_lines:
                m = _DATA_ATOM_RE.match(ln)
                if not m:
                    continue
                aid = m.group("id")
                if aid is None:
                    aid = f"_anon{anon}"
                    anon += 1
                eff_type = _apply_atom_replace_static(
                    m.group("type") or "", atom_replacements
                )
                lines.append(
                    f"    _a = tmpl.def_atom(name={aid!r}, type={eff_type!r}, "
                    f"charge={float(m.group('charge'))!r}, "
                    f"xyz=[{float(m.group('x'))!r}, {float(m.group('y'))!r}, "
                    f"{float(m.group('z'))!r}])"
                )
                lines.append(f"    atoms[{aid!r}] = _a")
        elif s.section in ("Data Bonds", "Data Bond List"):
            for ln in s.body_lines:
                m = _DATA_BOND_TYPED_RE.match(ln)
                if m:
                    lines.append(
                        f"    _a1 = _resolve_atom(atoms, {m.group('a')!r}); "
                        f"_a2 = _resolve_atom(atoms, {m.group('b')!r})"
                    )
                    lines.append("    if _a1 is not None and _a2 is not None:")
                    lines.append(
                        f"        tmpl.def_bond(_a1, _a2, type={m.group('type')!r})"
                    )
                    continue
                m = _DATA_BOND_LIST_RE.match(ln)
                if m:
                    lines.append(
                        f"    _a1 = _resolve_atom(atoms, {m.group('a')!r}); "
                        f"_a2 = _resolve_atom(atoms, {m.group('b')!r})"
                    )
                    lines.append("    if _a1 is not None and _a2 is not None:")
                    lines.append('        tmpl.def_bond(_a1, _a2, type="")')
        elif s.section == "Data Angles":
            for ln in s.body_lines:
                m = _DATA_ANGLE_RE.match(ln)
                if m:
                    lines.append(
                        f"    _a1 = _resolve_atom(atoms, {m.group('a')!r}); "
                        f"_a2 = _resolve_atom(atoms, {m.group('b')!r}); "
                        f"_a3 = _resolve_atom(atoms, {m.group('c')!r})"
                    )
                    lines.append("    if _a1 and _a2 and _a3:")
                    lines.append(
                        f"        tmpl.def_angle(_a1, _a2, _a3, type={m.group('type')!r})"
                    )
        elif s.section == "Data Dihedrals":
            for ln in s.body_lines:
                m = _DATA_DIHE_RE.match(ln)
                if m:
                    lines.append(
                        f"    _a1 = _resolve_atom(atoms, {m.group('a')!r}); "
                        f"_a2 = _resolve_atom(atoms, {m.group('b')!r}); "
                        f"_a3 = _resolve_atom(atoms, {m.group('c')!r}); "
                        f"_a4 = _resolve_atom(atoms, {m.group('d')!r})"
                    )
                    lines.append("    if _a1 and _a2 and _a3 and _a4:")
                    lines.append(
                        f"        tmpl.def_dihedral(_a1, _a2, _a3, _a4, "
                        f"type={m.group('type')!r})"
                    )
        elif s.section == "Data Impropers":
            for ln in s.body_lines:
                m = _DATA_IMPR_RE.match(ln)
                if m:
                    lines.append(
                        f"    _a1 = _resolve_atom(atoms, {m.group('a')!r}); "
                        f"_a2 = _resolve_atom(atoms, {m.group('b')!r}); "
                        f"_a3 = _resolve_atom(atoms, {m.group('c')!r}); "
                        f"_a4 = _resolve_atom(atoms, {m.group('d')!r})"
                    )
                    lines.append("    if _a1 and _a2 and _a3 and _a4:")
                    lines.append(
                        f"        tmpl.def_improper(_a1, _a2, _a3, _a4, "
                        f"type={m.group('type')!r})"
                    )
    lines.append("    return tmpl")
    return "\n".join(lines)


