_SET_CHARGE_RE = re.compile(
    r"^\s*set\s+type\s+@atom:(\S+)\s+charge\s+(-?\d+\.?\d*(?:[eE][+-]?\d+)?)\s*$"
)
_COEFF_RE = re.compile(
    r"""
    ^\s*(?P<kind>pair|bond|angle|dihedral|improper)_coeff\s+
    (?P<body>.+?)\s*$
    """,
    re.VERBOSE,
)
_AT_RE = re.compile(r"^@(?:atom|bond|angle|dihedral|improper|pair|mol):(.+)$")
_DOLLAR_RE = re.compile(r"^\$(?:atom|bond|angle|dihedral|improper|mol):(.+)$")
_KIND_PREFIX_RE = re.compile(r"^@(atom|bond|angle|dihedral|improper|pair|mol):")

_BY_TYPE_SECTIONS = {
    "Data Bonds By Type",
    "Data Angles By Type",
    "Data Dihedrals By Type",
    "Data Impropers By Type",
}

_DEFAULTS = {
    "bond": "harmonic",
    "angle": "harmonic",
    "dihedral": "opls",
    "improper": "harmonic",
    "pair": "lj/cut/coul/cut",
}


def _strip_prefix(tok: str) -> str:
    m = _AT_RE.match(tok) or _DOLLAR_RE.match(tok)
    return m.group(1) if m else tok


def _token_kind(tok: str) -> str | None:
    m = _KIND_PREFIX_RE.match(tok)
    return m.group(1) if m else None


def _split_line(line: str) -> list[str]:
    if "#" in line:
        line = line.split("#", 1)[0]
    return line.split()


def _split_style_params(tokens: list[str]) -> tuple[str | None, list[float]]:
    if not tokens:
        return None, []
    try:
        float(tokens[0])
        return None, [float(t) for t in tokens if _is_num(t)]
    except ValueError:
        return tokens[0], [float(t) for t in tokens[1:] if _is_num(t)]


def _is_num(s: str) -> bool:
    try:
        float(s)
        return True
    except ValueError:
        return False


def _collect_replace_pairs(flat: Iterable) -> dict[str, list[tuple[str, str]]]:
    out: dict[str, list[tuple[str, str]]] = {
        "atom": [],
        "bond": [],
        "angle": [],
        "dihedral": [],
        "improper": [],
    }
    for stmt in flat:
        if not isinstance(stmt, ReplaceStmt):
            continue
        for old, new in stmt.pairs:
            kind = _token_kind(old)
            if kind in out:
                out[kind].append((_strip_prefix(old), _strip_prefix(new)))
    return out


def _style_var(kind: str, style_name: str) -> str:
    sanitised = re.sub(r"[^A-Za-z0-9_]", "_", style_name)
    return f"_{kind}_style_{sanitised}"


def _emit_forcefield_builder(flat: list) -> str:
    lines: list[str] = [
        "def build_forcefield() -> mp.AtomisticForcefield:",
        '    """Reconstruct the force field from the original ``.lt`` file."""',
        '    ff = mp.AtomisticForcefield(name="moltemplate", units="real")',
        "    ff.metadata = {}",
        "    atom_style = ff.def_atomstyle('full')",
        "    def _atom(name, **kw):",
        "        existing = atom_style.get_type_by_name(name)",
        "        if existing is not None:",
        "            for k, v in kw.items():",
        "                existing[k] = v",
        "            return existing",
        "        return atom_style.def_type(name=name, type_=name, **kw)",
    ]
    preserved_sections: dict[str, list[str]] = {}
    replace_pairs: list[tuple[str, str]] = []
    bytype_lines: dict[str, list[str]] = {k: [] for k in _BY_TYPE_SECTIONS}

    for stmt in flat:
        if isinstance(stmt, ReplaceStmt):
            replace_pairs.extend(stmt.pairs)
            continue
        if not isinstance(stmt, (WriteBlock, WriteOnceBlock)):
            continue
        section = stmt.section
        if section == "Data Masses":
            lines.extend(_emit_data_masses(stmt.body_lines))
        elif section in ("Data Charges", "In Charges"):
            lines.extend(_emit_data_charges(stmt.body_lines))
        elif section == "In Settings":
            lines.extend(_emit_in_settings(stmt.body_lines))
        elif section in _BY_TYPE_SECTIONS:
            bytype_lines[section].extend(stmt.body_lines)
        else:
            preserved_sections.setdefault(section, []).extend(stmt.body_lines)

    for section, body in preserved_sections.items():
        lines.append(f"    ff.metadata.setdefault({section!r}, []).extend({body!r})")
    for section, body in bytype_lines.items():
        if body:
            lines.append(
                f"    ff.metadata.setdefault({section!r}, []).extend({body!r})"
            )

    if replace_pairs:
        lines.append("    ff.metadata['_replace_map'] = {")
        lines.append("        'atom': [], 'bond': [], 'angle': [],")
        lines.append("        'dihedral': [], 'improper': [],")
        lines.append("    }")
        for old, new in replace_pairs:
            kind = _token_kind(old)
            if kind is None:
                continue
            lines.append(
                f"    ff.metadata['_replace_map'][{kind!r}].append("
                f"({_strip_prefix(old)!r}, {_strip_prefix(new)!r}))"
            )

    lines.append("    return ff")
    return "\n".join(lines)


def _emit_data_masses(body: list[str]) -> list[str]:
    out: list[str] = []
    for ln in body:
        toks = _split_line(ln)
        if len(toks) < 2 or not toks[0].startswith("@atom:"):
            continue
        name = _strip_prefix(toks[0])
        try:
            mass = float(toks[1])
        except ValueError:
            continue
        out.append(f"    _atom({name!r}, mass={mass!r}, charge=0.0)")
    return out


def _emit_data_charges(body: list[str]) -> list[str]:
    out: list[str] = []
    for ln in body:
        m = _SET_CHARGE_RE.match(ln)
        if m:
            name, charge = m.group(1), float(m.group(2))
            out.append(f"    _atom({name!r}, charge={charge!r})")
    return out


def _emit_in_settings(body: list[str]) -> list[str]:
    out: list[str] = []
    style_created: set[tuple[str, str]] = set()
    for raw in body:
        m = _COEFF_RE.match(raw)
        if not m:
            continue
        kind = m.group("kind")
        toks = _split_line(m.group("body"))
        if len(toks) < 2:
            continue

        if kind == "pair":
            if not (toks[0].startswith("@atom:") and toks[1].startswith("@atom:")):
                continue
            name_i = _strip_prefix(toks[0])
            name_j = _strip_prefix(toks[1])
            if "*" in name_i or "*" in name_j:
                continue
            rest = toks[2:]
            style_name, params = _split_style_params(rest)
            style_name = style_name or _DEFAULTS[kind]
            var = _style_var(kind, style_name)
            stamp = (kind, style_name)
            if stamp not in style_created:
                out.append(f"    {var} = _def_style(ff, {kind!r}, {style_name!r})")
                style_created.add(stamp)
            out.append(
                f"    _def_coeff({var}, [_atom({name_i!r}), _atom({name_j!r})], "
                f"{params!r})"
            )
            continue

        # bond / angle / dihedral / improper
        prefix = _strip_prefix(toks[0])
        rest = toks[1:]
        style_name, params = _split_style_params(rest)
        style_name = style_name or _DEFAULTS.get(kind, "harmonic")
        var = _style_var(kind, style_name)
        stamp = (kind, style_name)
        if stamp not in style_created:
            out.append(f"    {var} = _def_style(ff, {kind!r}, {style_name!r})")
            style_created.add(stamp)
        arity = {"bond": 2, "angle": 3, "dihedral": 4, "improper": 4}[kind]
        placeholder = ", ".join(f"_atom({prefix + '_' + s!r})" for s in "ijkl"[:arity])
        out.append(
            f"    _def_coeff({var}, [{placeholder}], {params!r}, name={prefix!r})"
        )
    return out


