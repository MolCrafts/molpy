def _emit_document(doc: Document) -> str:
    classes: dict[str, ClassDef] = {}

    def collect(stmts):
        for s in stmts:
            if isinstance(s, ClassDef):
                classes[s.name] = s
                collect(s.statements)

    collect(doc.statements)

    flat = _flatten(doc)
    replace_rules = _collect_replace_pairs(flat)
    atom_replacements = _compile_atom_type_replacements(replace_rules["atom"])

    parts: list[str] = [_HEADER]
    parts.append(_emit_forcefield_builder(flat))
    for cls_name in _class_emission_order(classes, doc):
        parts.append(
            _emit_class_builder(
                classes[cls_name],
                class_map=classes,
                atom_replacements=atom_replacements,
            )
        )
    parts.append(_emit_system_builder(doc, classes))
    parts.append(_MAIN_BLOCK)
    return "\n\n".join(parts)


